import numpy as np

w = [[52.783, 52.779, 52.772, 52.766], # B-1
     [40.167, 40.193, 40.161, 40.148], # 1-2
     [49.818, 49.800, 49.816, 49.818], # 2-3
     [79.032, 79.055, 79.055], # 3-4
     [34.843, 34.839, 34.837, 34.834], # 4-5
     [59.293, 59.293, 59.303, 59.295], # 5-C
     # [179.541, 179.564, 179.530, 179.523], # C-D (ale osnowa nie podlega wyrównaniu)
     [128.748, 128.757, 128.744, 128.753], # 2-4
     [81.357, 81.350, 81.356, 81.336]  # 4-C
    ]

for p in w:
    w_sr = 1/len(p)*sum(p)
    r = [(i - w_sr)**2 for i in p]
    m0 = np.sqrt(sum(r) / (len(p) - 1))
    print(f"HD_sr = {w_sr} m", m0, f"m0 ~ {m0*1000:.0f} mm")